// Helper function to get districts for a city in a specific language
export function getDistrictsForCity(city, language = 'en') {
  const cityKey = city.toLowerCase();
  if (!DISTRICTS[cityKey]) {
    return [];
  }
  return DISTRICTS[cityKey][language] || [];
}

// Helper function to get all cities with districts
export function getCitiesWithDistricts() {
  return Object.keys(DISTRICTS);
}

// Helper function to validate if a district exists for a city
export function isValidDistrict(city, district, language = 'en') {
  const districts = getDistrictsForCity(city, language);
  return districts.includes(district);
}

// Helper function to convert localized district to English
export function getEnglishDistrict(city, localizedDistrict, currentLanguage = 'en') {
  if (currentLanguage === 'en') {
    return localizedDistrict; // Already in English
  }
  
  const cityKey = city.toLowerCase();
  if (!DISTRICTS[cityKey]) {
    return localizedDistrict;
  }
  
  const localizedDistricts = DISTRICTS[cityKey][currentLanguage] || [];
  const englishDistricts = DISTRICTS[cityKey].en || [];
  
  const index = localizedDistricts.indexOf(localizedDistrict);
  if (index !== -1 && index < englishDistricts.length) {
    return englishDistricts[index];
  }
  
  return localizedDistrict; // Fallback to original if not found
}

// Helper function to convert English district to localized version
export function getLocalizedDistrict(city, englishDistrict, targetLanguage = 'en') {
  if (targetLanguage === 'en') {
    return englishDistrict; // Already in English
  }
  
  const cityKey = city.toLowerCase();
  if (!DISTRICTS[cityKey]) {
    return englishDistrict;
  }
  
  const englishDistricts = DISTRICTS[cityKey].en || [];
  const localizedDistricts = DISTRICTS[cityKey][targetLanguage] || [];
  
  const index = englishDistricts.indexOf(englishDistrict);
  if (index !== -1 && index < localizedDistricts.length) {
    return localizedDistricts[index];
  }
  
  return englishDistrict; // Fallback to English if not found
}